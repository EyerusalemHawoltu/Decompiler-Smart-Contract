"""
eval_cfg_csv.py
===============
End-to-end CFG evaluation pipeline:
  1. Load bytecode (.hex) for each contract
  2. Run evm_cfg_builder to extract CFG function names
  3. Parse matching Solidity source (.sol.cleaned) for public/external functions
  4. Compute: precision, recall, F1, Jaccard, BLEU-1..4, exact match, count recall
  5. Write per-version CSV files and a combined summary CSV

Output layout:
  results/cfg_eval/
    all_results.csv           -- one row per contract (all versions combined)
    {version}_results.csv     -- per-version rows (e.g. 0.8.9_results.csv)
    version_summary.csv       -- one aggregate row per version
    cfg_eval_{jobid}.out      -- SLURM stdout (written by SLURM)

Each CSV row contains:
  version, address, contract_name,
  n_cfg_total, n_cfg_named, n_sol_public, n_matched,
  precision, recall, f1, jaccard, exact_match, count_recall,
  bleu1, bleu2, bleu3, bleu4, bleu,
  cfg_functions   (pipe-separated named CFG list),
  sol_functions   (pipe-separated Solidity public list),
  matched         (functions in both),
  missed          (in Solidity, not in CFG),
  extra           (in CFG, not in Solidity),
  status, error

Notes on methodology:
  - Precision = named CFG functions that appear in Solidity public set
  - Recall    = deployed Solidity public functions found by name in CFG
  - The Solidity ground truth counts ONLY functions that are actually deployed:
    functions declared inside `interface` blocks (IUniswapV2Router, IERC20, …)
    are excluded because they are external ABIs, never compiled into the
    contract's own bytecode.  Counting them previously deflated recall by ~30%.
  - Remaining missed functions are mostly OZ base-class functions evm_cfg_builder
    cannot follow into inherited dispatch tables
  - Extra functions are often public state-variable getters (e.g. uniswapV2Pair)
    auto-generated by the compiler; our regex only matches explicit `function`
    declarations so these appear as CFG-only hits

Usage:
  python eval_cfg_csv.py                    # all contracts, 1 worker
  python eval_cfg_csv.py --workers 16       # parallel (HPC)
  python eval_cfg_csv.py --version 0.8.9    # single version
  python eval_cfg_csv.py --limit 500        # quick test
"""

import argparse
import csv
import math
import os
import re
import sys
import time
from collections import defaultdict
from concurrent.futures import ProcessPoolExecutor, as_completed

# ── Paths ─────────────────────────────────────────────────────────────────────
ROOT         = os.path.dirname(os.path.abspath(__file__))
BYTECODE_DIR = os.path.join(ROOT, 'Contracts_Bytecode')
SOLIDITY_DIR = os.path.join(ROOT, 'Contracts_By_Version_Cleaned')
# CFG_EVAL_OUT lets a baseline / ablation run write to a separate folder
# without overwriting the production results.
RESULTS_DIR  = os.environ.get('CFG_EVAL_OUT', os.path.join(ROOT, 'results', 'cfg_eval'))

def _setup_path():
    for p in (os.path.join(ROOT, 'evm_cfg_builder'), os.path.join(ROOT, 'nova')):
        if os.path.isdir(p) and p not in sys.path:
            sys.path.insert(0, p)

_setup_path()


# ── BLEU (pure Python, no external deps) ─────────────────────────────────────
def _ngrams(tokens, n):
    counts = {}
    for i in range(len(tokens) - n + 1):
        g = tuple(tokens[i:i+n])
        counts[g] = counts.get(g, 0) + 1
    return counts


def bleu_score(hypothesis, reference, max_n=4):
    """Sentence BLEU treating each function name as one token."""
    log_sum = 0.0
    valid   = 0
    scores  = {}
    for n in range(1, max_n + 1):
        hyp_ng  = _ngrams(hypothesis, n)
        ref_ng  = _ngrams(reference,  n)
        clipped = sum(min(c, ref_ng.get(g, 0)) for g, c in hyp_ng.items())
        total   = max(sum(hyp_ng.values()), 1)
        prec    = clipped / total if total > 0 else 0.0
        scores[f'bleu{n}'] = round(prec, 4)
        if prec > 0:
            log_sum += math.log(prec)
            valid   += 1
    bp       = 1.0 if len(hypothesis) >= len(reference) else \
               math.exp(1 - len(reference) / max(len(hypothesis), 1))
    geo_mean = math.exp(log_sum / max_n) if valid == max_n else 0.0
    scores['bleu'] = round(bp * geo_mean, 4)
    return scores


# ── Solidity parser ───────────────────────────────────────────────────────────
# Counts public/external functions that are ACTUALLY DEPLOYED.
#
# A flattened sol.cleaned file contains the deployed contract plus every
# imported interface (IUniswapV2Router, IERC20, …), library and base contract.
# Functions declared inside `interface` blocks are *never* part of the deployed
# bytecode — they are ABIs used to call OTHER contracts — so counting them as
# ground-truth public functions artificially deflates recall.  We therefore:
#   1. strip comments (avoids false 'function' matches inside docstrings)
#   2. brace-match each top-level contract/interface/library block
#   3. count public/external functions only from non-interface blocks
#
# Public state-variable getters (address public foo;) are still NOT matched
# — they appear as CFG-only 'extra' functions.

_BLOCK_RE = re.compile(r'\b(abstract\s+contract|contract|interface|library)\s+(\w+)')
_FN_RE    = re.compile(r'\bfunction\s+(\w+)\s*\([^)]*\)[^{;]*?\b(public|external)\b')


def _strip_comments(src):
    src = re.sub(r'/\*.*?\*/', '', src, flags=re.DOTALL)   # block comments
    src = re.sub(r'//[^\n]*', '', src)                      # line comments
    return src


def _iter_blocks(src):
    """Yield (kind, name, body) for each top-level contract/interface/library."""
    for m in _BLOCK_RE.finditer(src):
        kind  = m.group(1).split()[-1]   # contract / interface / library
        name  = m.group(2)
        brace = src.find('{', m.end())
        if brace == -1:
            continue
        depth = 0
        i = brace
        while i < len(src):
            c = src[i]
            if c == '{':
                depth += 1
            elif c == '}':
                depth -= 1
                if depth == 0:
                    break
            i += 1
        yield kind, name, src[brace:i]


def parse_sol_functions(sol_path):
    """Return set of deployed public/external function names (interfaces excluded)."""
    try:
        with open(sol_path) as f:
            src = _strip_comments(f.read())
        fns = set()
        for kind, _name, body in _iter_blocks(src):
            if kind == 'interface':
                continue   # interface functions are never deployed
            for m in _FN_RE.finditer(body):
                fns.add(m.group(1))
        return fns
    except Exception:
        return set()


# ── Bytecode classifier ───────────────────────────────────────────────────────
def classify_bytecode(bc: str) -> str:
    """Categorise raw runtime bytecode.

    'library_stub' — a deployed Solidity *library*: an 86-byte
        `PUSH20 <self-addr>; ADDRESS; EQ; ...` delegatecall guard.  Library
        functions are `internal` and inlined into callers, so the deployed
        stub has NO callable functions by construction.  Scoring these against
        a full multi-contract source file is meaningless, so they are excluded
        from the headline averages.
    'empty'  — no bytecode.
    'tiny'   — too short to hold a dispatcher (<= 80 bytes, not a stub).
    'runtime'— a normal contract runtime with a function dispatcher.
    """
    bc = (bc or '').strip().lower()
    if not bc:
        return 'empty'
    # library stub: PUSH20 + 20 zero-bytes (placeholder addr) + ADDRESS EQ
    if bc.startswith('73') and bc[2:42] == '0' * 40 and bc[42:46] == '3014':
        return 'library_stub'
    if len(bc) <= 160:           # <= 80 bytes
        return 'tiny'
    return 'runtime'


# ── CSV schema ────────────────────────────────────────────────────────────────
CSV_FIELDS = [
    'version', 'address', 'contract_name',
    'bytecode_type', # runtime / library_stub / tiny / empty
    # Raw counts
    'n_cfg_total',   # all CFG functions (named + hex IDs, excl. fallback/dispatcher)
    'n_cfg_named',   # functions with a resolved name
    'n_sol_public',  # public/external functions in Solidity source
    'n_matched',     # names in both CFG and Solidity
    # Scores
    'precision', 'recall', 'f1', 'jaccard', 'exact_match', 'count_recall',
    'bleu1', 'bleu2', 'bleu3', 'bleu4', 'bleu',
    # Name lists (pipe-separated)
    'cfg_functions',  # named CFG functions
    'sol_functions',  # Solidity public/external functions
    'matched',        # intersection
    'missed',         # sol_only  (in Solidity, NOT in CFG)
    'extra',          # cfg_only  (in CFG, NOT in Solidity)
    'status', 'error',
]

METRICS = [
    'precision', 'recall', 'f1', 'jaccard', 'exact_match',
    'count_recall', 'bleu1', 'bleu2', 'bleu3', 'bleu4', 'bleu',
]


# ── Per-contract worker (also used as subprocess target) ──────────────────────
def process(job):
    hex_path, sol_path, version, address, contract_name = job
    _setup_path()   # needed in each subprocess worker

    row = {f: '' for f in CSV_FIELDS}
    row.update({
        'version':       version,
        'address':       address,
        'contract_name': contract_name,
        'bytecode_type': 'unknown',
        'status':        'ok',
        'error':         '',
        'n_cfg_total':   0,
        'n_cfg_named':   0,
        'n_sol_public':  0,
        'n_matched':     0,
        **{m: 0.0 for m in METRICS},
    })

    # ── 1. Parse Solidity source ──────────────────────────────────────────────
    sol_fns = parse_sol_functions(sol_path)
    row['sol_functions'] = '|'.join(sorted(sol_fns))
    row['n_sol_public']  = len(sol_fns)

    # ── 2. Extract CFG ────────────────────────────────────────────────────────
    # CFG_HARD_TIMEOUT (seconds) installs a SIGALRM that aborts a hung extraction
    # — needed for the pristine baseline, whose VSA can infinite-loop on 0.8.x.
    _hard_to = int(os.environ.get('CFG_HARD_TIMEOUT', '0'))
    if _hard_to > 0:
        import signal
        def _alarm(signum, frame):
            raise TimeoutError(f'hard timeout {_hard_to}s')
        try:
            signal.signal(signal.SIGALRM, _alarm)
            signal.alarm(_hard_to)
        except (ValueError, AttributeError):
            _hard_to = 0   # not in main thread / unsupported
    try:
        with open(hex_path) as f:
            bytecode = f.read().strip()
        row['bytecode_type'] = classify_bytecode(bytecode)
        if not bytecode:
            row['status'] = 'empty'
            return row

        # CFG_EXTRACTOR selects the front-end:
        #   'cfg'      (default) -> evm_cfg_builder full CFG
        #   'selector'           -> lightweight dispatcher selector scan
        if os.environ.get('CFG_EXTRACTOR', 'cfg') == 'selector':
            from selector_extract import extract_functions_by_selector
            cfgs = extract_functions_by_selector(bytecode)
        else:
            from bytecode_to_cfg import extract_function_cfgs
            cfgs = extract_function_cfgs(bytecode)

        all_fns   = [n for n in cfgs if n not in ('_fallback', '_dispatcher')]
        named_fns = {n for n in all_fns
                     if not n.startswith('0x') and not n.startswith('func_')}
    except Exception as e:
        row['status'] = 'error'
        row['error']  = str(e)[:200]
        return row
    finally:
        if _hard_to > 0:
            import signal
            signal.alarm(0)

    row['n_cfg_total']   = len(all_fns)
    row['n_cfg_named']   = len(named_fns)
    row['cfg_functions'] = '|'.join(sorted(named_fns))

    if not all_fns and not sol_fns:
        row['status'] = 'both_empty'
        return row

    # ── 3. Compute metrics ────────────────────────────────────────────────────
    matched  = named_fns & sol_fns
    cfg_only = named_fns - sol_fns
    sol_only = sol_fns  - named_fns

    row['n_matched'] = len(matched)
    row['matched']   = '|'.join(sorted(matched))
    row['missed']    = '|'.join(sorted(sol_only))   # in Sol, not in CFG
    row['extra']     = '|'.join(sorted(cfg_only))   # in CFG, not in Sol

    precision    = len(matched) / len(named_fns) if named_fns else 0.0
    recall       = len(matched) / len(sol_fns)   if sol_fns   else 0.0
    f1           = (2 * precision * recall / (precision + recall)
                    if precision + recall > 0 else 0.0)
    union        = named_fns | sol_fns
    jaccard      = len(matched) / len(union) if union else 0.0
    exact        = 1 if named_fns == sol_fns else 0
    count_recall = min(len(all_fns), len(sol_fns)) / len(sol_fns) \
                   if sol_fns else 0.0
    bleu         = bleu_score(sorted(named_fns), sorted(sol_fns))

    row.update({
        'precision':    round(precision, 4),
        'recall':       round(recall, 4),
        'f1':           round(f1, 4),
        'jaccard':      round(jaccard, 4),
        'exact_match':  exact,
        'count_recall': round(count_recall, 4),
        **{k: round(v, 4) for k, v in bleu.items()},
    })
    return row


# ── Job collection ────────────────────────────────────────────────────────────
def collect_jobs(version_filter=None, limit=None, sample_per_version=None):
    jobs = []
    for version in sorted(os.listdir(BYTECODE_DIR)):
        if version_filter and version != version_filter:
            continue
        hex_dir = os.path.join(BYTECODE_DIR, version)
        sol_dir = os.path.join(SOLIDITY_DIR, version)
        if not os.path.isdir(hex_dir) or not os.path.isdir(sol_dir):
            continue
        n_ver = 0
        for fname in sorted(os.listdir(hex_dir)):
            if not fname.endswith('.hex'):
                continue
            base     = fname[:-4]
            sol_file = os.path.join(sol_dir, base + '.sol.cleaned')
            if not os.path.isfile(sol_file):
                continue
            parts         = base.split('_', 1)
            address       = parts[0]
            contract_name = parts[1] if len(parts) > 1 else base
            jobs.append((
                os.path.join(hex_dir, fname),
                sol_file,
                version,
                address,
                contract_name,
            ))
            n_ver += 1
            if sample_per_version and n_ver >= sample_per_version:
                break   # cap this version (deterministic: first N by name)
    if limit:
        jobs = jobs[:limit]
    return jobs


# ── Main ──────────────────────────────────────────────────────────────────────
def run(version_filter=None, limit=None, workers=1, sample_per_version=None):
    os.makedirs(RESULTS_DIR, exist_ok=True)

    jobs = collect_jobs(version_filter, limit, sample_per_version)
    print(f'Contracts to evaluate : {len(jobs)}')
    print(f'Workers               : {workers}')
    print(f'Output directory      : {RESULTS_DIR}')
    print()

    # ── Open writers ─────────────────────────────────────────────────────────
    all_csv_path = os.path.join(RESULTS_DIR, 'all_results.csv')
    all_f        = open(all_csv_path, 'w', newline='', encoding='utf-8')
    all_w        = csv.DictWriter(all_f, fieldnames=CSV_FIELDS)
    all_w.writeheader()

    ver_files   = {}   # version -> file handle
    ver_writers = {}   # version -> DictWriter
    ver_sums    = defaultdict(lambda: defaultdict(float))
    ver_counts  = defaultdict(int)
    # runtime-only (library stubs / empty / tiny excluded) — the fair headline
    rt_sums     = defaultdict(lambda: defaultdict(float))
    rt_counts   = defaultdict(int)
    stub_counts = defaultdict(int)

    def _get_ver_writer(ver):
        if ver not in ver_writers:
            path           = os.path.join(RESULTS_DIR, f'{ver}_results.csv')
            fh             = open(path, 'w', newline='', encoding='utf-8')
            w              = csv.DictWriter(fh, fieldnames=CSV_FIELDS)
            w.writeheader()
            ver_files[ver] = fh
            ver_writers[ver] = w
        return ver_writers[ver]

    # ── Counters ──────────────────────────────────────────────────────────────
    n_done = n_ok = n_err = n_empty = 0
    t0 = time.time()

    def _record(row):
        nonlocal n_done, n_ok, n_err, n_empty
        n_done += 1
        ver = row['version']

        all_w.writerow(row)
        _get_ver_writer(ver).writerow(row)

        btype = row.get('bytecode_type', 'unknown')
        if btype == 'library_stub':
            stub_counts[ver] += 1

        s = row.get('status', 'error')
        if s == 'ok':
            n_ok += 1
            for m in METRICS:
                v = row.get(m, 0)
                fv = float(v) if v != '' else 0.0
                ver_sums[ver][m] += fv
                if btype == 'runtime':
                    rt_sums[ver][m] += fv
            ver_counts[ver] += 1
            if btype == 'runtime':
                rt_counts[ver] += 1
        elif s == 'error':
            n_err += 1
        else:
            n_empty += 1

        if n_done % 1000 == 0 or n_done == len(jobs):
            rate    = n_done / (time.time() - t0)
            eta     = (len(jobs) - n_done) / rate if rate > 0 else 0
            tot_ok  = sum(ver_counts.values())
            ap = sum(ver_sums[v]['precision'] for v in ver_sums) / max(tot_ok, 1)
            ar = sum(ver_sums[v]['recall']    for v in ver_sums) / max(tot_ok, 1)
            af = sum(ver_sums[v]['f1']        for v in ver_sums) / max(tot_ok, 1)
            ab = sum(ver_sums[v]['bleu']      for v in ver_sums) / max(tot_ok, 1)
            print(f'  [{n_done:>7}/{len(jobs)}]  '
                  f'P={ap:.3f}  R={ar:.3f}  F1={af:.3f}  BLEU={ab:.3f}  '
                  f'ok={n_ok} err={n_err}  {rate:.0f}/s  ETA {eta/60:.1f}min')
            sys.stdout.flush()
            all_f.flush()
            for fh in ver_files.values():
                fh.flush()

    # ── Process ───────────────────────────────────────────────────────────────
    if workers > 1:
        with ProcessPoolExecutor(max_workers=workers) as pool:
            futures = {pool.submit(process, j): j for j in jobs}
            for fut in as_completed(futures):
                try:
                    row = fut.result(timeout=60)
                except Exception as e:
                    j   = futures[fut]
                    row = {f: '' for f in CSV_FIELDS}
                    row.update({'version': j[2], 'address': j[3],
                                'contract_name': j[4],
                                'status': 'error', 'error': str(e)[:200]})
                _record(row)
    else:
        for job in jobs:
            _record(process(job))

    # ── Close per-version files ───────────────────────────────────────────────
    all_f.close()
    for fh in ver_files.values():
        fh.close()

    elapsed = time.time() - t0
    tot_ok  = sum(ver_counts.values())
    ap  = sum(ver_sums[v]['precision']    for v in ver_sums) / max(tot_ok, 1)
    ar  = sum(ver_sums[v]['recall']       for v in ver_sums) / max(tot_ok, 1)
    af  = sum(ver_sums[v]['f1']           for v in ver_sums) / max(tot_ok, 1)
    ab  = sum(ver_sums[v]['bleu']         for v in ver_sums) / max(tot_ok, 1)
    acr = sum(ver_sums[v]['count_recall'] for v in ver_sums) / max(tot_ok, 1)
    aem = sum(ver_sums[v]['exact_match']  for v in ver_sums) / max(tot_ok, 1)

    # ── version_summary.csv (ALL contracts) ───────────────────────────────────
    summary_path = os.path.join(RESULTS_DIR, 'version_summary.csv')
    sum_fields   = ['version', 'n_contracts'] + METRICS
    with open(summary_path, 'w', newline='', encoding='utf-8') as sf:
        sw = csv.DictWriter(sf, fieldnames=sum_fields)
        sw.writeheader()
        for ver in sorted(ver_sums.keys()):
            n = ver_counts[ver]
            sw.writerow({
                'version':     ver,
                'n_contracts': n,
                **{m: round(ver_sums[ver][m] / n, 4) if n else 0.0
                   for m in METRICS},
            })

    # ── version_summary_no_stubs.csv (runtime contracts only) ──────────────────
    # Excludes library stubs / empty / tiny bytecode — contracts that cannot
    # contain recoverable functions by construction.  This is the fair headline.
    rt_path   = os.path.join(RESULTS_DIR, 'version_summary_no_stubs.csv')
    rt_fields = ['version', 'n_runtime', 'n_stubs_excluded'] + METRICS
    with open(rt_path, 'w', newline='', encoding='utf-8') as sf:
        sw = csv.DictWriter(sf, fieldnames=rt_fields)
        sw.writeheader()
        for ver in sorted(ver_sums.keys()):
            n = rt_counts[ver]
            sw.writerow({
                'version':          ver,
                'n_runtime':        n,
                'n_stubs_excluded': stub_counts[ver],
                **{m: round(rt_sums[ver][m] / n, 4) if n else 0.0
                   for m in METRICS},
            })

    # runtime-only weighted headline
    rt_tot = sum(rt_counts.values())
    rt_ap  = sum(rt_sums[v]['precision'] for v in rt_sums) / max(rt_tot, 1)
    rt_ar  = sum(rt_sums[v]['recall']    for v in rt_sums) / max(rt_tot, 1)
    rt_af  = sum(rt_sums[v]['f1']        for v in rt_sums) / max(rt_tot, 1)

    print(f"""
Done in {elapsed/60:.1f} min

╔══════════════════════════════════════════════════════╗
║         CFG Evaluation — Final Scores               ║
╠══════════════════════════════════════════════════════╣
  Contracts evaluated  : {tot_ok:,} / {len(jobs):,}
  Errors               : {n_err}
  Empty / no-func      : {n_empty}
╠══════════════════════════════════════════════════════╣
  Precision            : {ap:.4f}
  Recall               : {ar:.4f}
  F1 Score             : {af:.4f}
  BLEU (combined)      : {ab:.4f}
  Count Recall         : {acr:.4f}
  Exact Match          : {aem:.2%}
╠══════════════════════════════════════════════════════╣
  RUNTIME-ONLY (library stubs excluded) — fair headline
  Runtime contracts     : {rt_tot:,}
  Precision            : {rt_ap:.4f}
  Recall               : {rt_ar:.4f}
  F1 Score             : {rt_af:.4f}
╠══════════════════════════════════════════════════════╣
  Output files:
    {all_csv_path}
    {RESULTS_DIR}/{{version}}_results.csv  (one per version)
    {summary_path}
    {rt_path}
╚══════════════════════════════════════════════════════╝""")


if __name__ == '__main__':
    ap = argparse.ArgumentParser(description='EVM CFG evaluation → CSV')
    ap.add_argument('--version', default=None,
                    help='Evaluate one pragma version only (e.g. 0.8.9)')
    ap.add_argument('--workers', type=int, default=1,
                    help='Parallel workers (default 1; use 16 on HPC)')
    ap.add_argument('--limit',   type=int, default=None,
                    help='Cap total contracts for a quick test')
    ap.add_argument('--sample',  type=int, default=None,
                    help='Cap contracts PER VERSION (balanced sample)')
    args = ap.parse_args()
    run(version_filter=args.version, limit=args.limit, workers=args.workers,
        sample_per_version=args.sample)
